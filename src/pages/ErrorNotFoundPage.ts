// ================================================================================
// * Module dependencies
// --------------------------------------------------------------------------------
import { computed, defineComponent } from 'vue';
import { useRoute } from 'vue-router';
// ================================================================================

export default defineComponent({
  setup() {
    const _route = useRoute();
    // ------------------------------------------------------------------------------
    // * Option
    // ------------------------------------------------------------------------------
    const currentPath = computed<string>(() => _route.path );
    // ------------------------------------------------------------------------------
    // * Function
    // ------------------------------------------------------------------------------

    return {
      // ------------------------------------------------------------------------------
      // * Parameter
      // ------------------------------------------------------------------------------
      currentPath,
      // ------------------------------------------------------------------------------
      // * Function
      // ------------------------------------------------------------------------------
    }

  },
});
