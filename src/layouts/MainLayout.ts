// ================================================================================
// * Module dependencies
// --------------------------------------------------------------------------------
import { computed, defineComponent, ref } from 'vue';
import { useRouter } from 'vue-router';
// --------------------------------------------------------------------------------
import LeftDrawer from 'layouts/item/LeftDrawer.vue';
// ================================================================================

export default defineComponent({
  components:{
    LeftDrawer
  },
  setup() {
    // ------------------------------------------------------------------------------
    // * Vue
    // ------------------------------------------------------------------------------
    const _router = useRouter();
    // ------------------------------------------------------------------------------
    // * Option
    // ------------------------------------------------------------------------------
    const routeName = computed<string>(() => _router.currentRoute.value.name as string);
    const routeIcon = computed<string>(() => _router.currentRoute.value.meta.icon as string);
    // ------------------------------------------------------------------------------
    // * Parameter
    // ------------------------------------------------------------------------------
    const leftDrawerOpen = ref<boolean>(false)
    // ------------------------------------------------------------------------------
    // * Function
    // ------------------------------------------------------------------------------
    //# region Button Events
    // ------------------------------------------------------------------------------
    function toggleLeftDrawer() {
      leftDrawerOpen.value = !leftDrawerOpen.value
    }
    // ------------------------------------------------------------------------------
    function openGithubPage() {
      window.open('', '_blank');
    }
    // ------------------------------------------------------------------------------
    //# endregion
    // ------------------------------------------------------------------------------

    return {
      // ------------------------------------------------------------------------------
      // * Option
      // ------------------------------------------------------------------------------
      routeName,
      routeIcon,
      // ------------------------------------------------------------------------------
      // * Parameter
      // ------------------------------------------------------------------------------
      leftDrawerOpen,
      // ------------------------------------------------------------------------------
      // * Function
      // ------------------------------------------------------------------------------
      toggleLeftDrawer,
      openGithubPage,
    }
  }
});
